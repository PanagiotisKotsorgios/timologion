export function getGuide(slug: string): Guide | null {
  return GUIDES.find((g) => g.slug === slug) ?? null;
}

export function nextGuide(slug: string): Guide | null {
  const idx = GUIDES.findIndex((g) => g.slug === slug);
  if (idx < 0 || idx >= GUIDES.length - 1) return null;
  return GUIDES[idx + 1]!;
}

export function prevGuide(slug: string): Guide | null {
  const idx = GUIDES.findIndex((g) => g.slug === slug);
  if (idx <= 0) return null;
  return GUIDES[idx - 1]!;
}
